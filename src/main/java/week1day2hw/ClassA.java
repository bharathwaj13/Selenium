package week1day2hw;

public class ClassA {
	int add(int n1,int n2) 
	{
		int result=n1+n2;
		return result;
	}
	int subtract(int n1,int n2)
	{
		int result=n1-n2;
		return result;
	}
	int multiply(int n1,int n2)
	{
		int result=n1*n2;
		return result;
	}
	int divide(int n1,int n2)
	{
		int result=n1/n2;
		return result;
	}

}
